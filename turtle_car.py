import turtle

t = turtle.Turtle()

t.color('yellow')
t.fillcolor('yellow')

t.penup()
t.goto(0, 0)
t.pendown()
t.begin_fill()
t.forward(185)
t.left(90)
t.forward(25)
t.left(90)
t.forward(185)
t.left(90)
t.forward(25)
t.end_fill()

t.penup()
t.goto(50, 25)
t.pendown()
t.color('blue')
t.fillcolor('')
t.setheading(45)
t.forward(35)
t.setheading(0)
t.forward(50)
t.setheading(-45)
t.forward(35)
t.setheading(90)
t.penup()
t.goto(100, 25)
t.pendown()
t.forward(25)

t.penup()
t.goto(50, -5)
t.pendown()
t.color('#000000')
t.fillcolor('#000000')
t.begin_fill()
t.circle(10)
t.end_fill()

t.penup()
t.goto(150, -5)
t.pendown()
t.color('#000000')
t.fillcolor('#000000')
t.begin_fill()
t.circle(10)
t.end_fill()

t.hideturtle()

turtle.mainloop()
